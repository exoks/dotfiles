-- LSP keymaps --
local keymap = vim.keymap

keymap.set("n", "D", vim.lsp.buf.hover, {}) -- hover
keymap.set("n", "gd", vim.lsp.buf.definition, {}) -- definition
keymap.set("n", "<leader>ca", vim.lsp.buf.code_action, {}) -- code action
keymap.set("n", "<leader>lf", vim.lsp.buf.format, {}) -- formatter
keymap.set("n", "<leader>d", vim.diagnostic.open_float, {}) -- diagnostics

