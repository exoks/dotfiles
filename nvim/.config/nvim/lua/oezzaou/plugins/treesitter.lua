return {
  -- plugin to generate AST used for highlights --
  "nvim-treesitter/nvim-treesitter",
  lazy = false,
  build = ":TSUpdate",

  -- treesitter config --
  config = function()
    local ts = require("nvim-treesitter")

    ts.install({
      "lua",
      "vim",
      "python",
      "bash",
      "javascript",
      "typescript",
      "html",
      "css",
      "json",
      "toml",
      "yaml",
      "c",
    })

    vim.api.nvim_create_autocmd("FileType", {
      pattern = {
        "lua",
        "vim",
        "python",
        "bash",
        "javascript",
        "typescript",
        "html",
        "css",
        "json",
        "toml",
        "yaml",
        "c",
        "cpp"
      },

      callback = function(args)
        pcall(vim.treesitter.start, args.buf)
      end,

    })

  end,
}

