-- ===[ neocodeium: ]==========================================================
return {
	"monkoose/neocodeium",
	event = "VeryLazy",
	config = function()
		local neocodeium = require("neocodeium")
		neocodeium.setup()

    -- keymaps --
    vim.keymap.set("i", "<C-g>", neocodeium.accept) -- Ctrl + g
    vim.keymap.set("i", "<C-;>", neocodeium.cycle_or_complete) -- Ctrl + ;
    vim.keymap.set("i", "<C-x>", neocodeium.clear) -- Ctrl + x
	end,
}
