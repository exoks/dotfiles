-- ===[ telescope: ]===========================================================
return {
  {
    "nvim-telescope/telescope.nvim",
    dependencies = {
      "nvim-lua/plenary.nvim",
    },
    opts = {
      -- default configuration --
      defaults = {
        prompt_prefix = "❯ ", -- Icon before the input field
        selection_caret = "➤ ", -- Icon for the current selection
        entry_prefix = "  ", -- Icon or padding before entries
        sorting_strategy = "ascending", -- Optional: Change sorting strategy
      },
      -- pickers configuration --
      pickers = {
        find_files = { hidden = true }, -- search inside hidden directories
      },
    },
    config = function(_, opts)
      require('telescope').setup(opts)
      -- telescope keymaps --
      local builtin = require("telescope.builtin")
      vim.keymap.set("n", "<leader>f", builtin.find_files, {})
    end,
  },
}
