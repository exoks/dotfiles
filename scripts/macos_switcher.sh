#====<[ script: wallpaper switcher ]>==========================================
#!/bin/bash

# A varibale that store the dir's wallpapers.
wallpaper_dir="/Users/oezzaou/dotfiles/hypr/.config/backgrounds"

# INFO: You need to install 'coreutils' to use it, run brew install  coeutils
# wallpaper=$(find "$wallpaper_dir" \
# -type f \( -iname "*.jpg" -o -iname "*.png" \) | gshuf -n 1)

# This command choses a random wallpaper from $wallpaper_dir directory 
wallpaper=$(find "$wallpaper_dir" \
  -type f \( -iname "*.jpg" -o -iname "*.png" \) | sort -R | head -n 1)

# NOTE:====================================================================
# - I am using 'sort -R' to chose a random wallpaper, You could use       | 
# 'gshuf' also to do that, I have chosen 'sort -R' instead, because it    | 
# does not require any additional or external packages.                   |
# =========================================================================

# A simple command for debuging purposes. 
echo "switcher: set up ${wallpaper} wallpaper"

# INFO:====================================================================
# - wal (pywal) commands generates a set of colors based on background    |
# wallpaper                                                               |
# =========================================================================
 
$HOME/Library/Python/3.7/bin/wal --backend haishoku -i ${wallpaper}

